    private void tbLugarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbLugarMouseClicked
        // TODO add your handling code here:
        int row = tbLugar.rowAtPoint(evt.getPoint());
        lbId.setText(tbLugar.getValueAt(row, 0).toString());
        txtNombre.setText(tbLugar.getValueAt(row, 1).toString());
        String tipo = tbLugar.getValueAt(row, 2).toString();
        for (int i = 0; i < cbTipo.getItemCount(); i++) {
            if (tipo.equals(cbTipo.getItemAt(i))) {
                cbTipo.setSelectedIndex(i);
            }
        }
    }//GEN-LAST:event_tbLugarMouseClicked

