    public void actualizar() {
        tbLugar.setModel(negocio.listarTabla());
        txtNombre.setText("");
        lbId.setText("");
        cbTipo.setSelectedIndex(0);
    }

    private void guardar() {

        try {
            if (!"".equals(txtNombre.getText())) {
                negocio.guardar(txtNombre.getText(), (String) cbTipo.getSelectedItem());
                JOptionPane.showMessageDialog(null, "Registro Guardado");
            } else {
                JOptionPane.showMessageDialog(null, "Revise que esten llenos los campos necesarios");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Hay un problema al registrar");
        }
        actualizar();
    }

    private void modificar() {
        if (!"".equals(lbId.getText()) && !txtNombre.getText().equals("")) {
            try {
                negocio.modificar(
                        Integer.valueOf(lbId.getText()),
                        txtNombre.getText(),
                        (String) cbTipo.getSelectedItem());
                JOptionPane.showMessageDialog(null, "Registro Actualizado");
                actualizar();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Hay un problema al actualizar");
            }
        } else {
            JOptionPane.showMessageDialog(null, "El campo Lugar no esta lleno", "Atencion", 1);
        }
    }

    private void eliminar() {

        try {
            if (!"".equals(lbId.getText())) {
                negocio.eliminar(Integer.valueOf(lbId.getText()));
                actualizar();
                JOptionPane.showMessageDialog(null, "Registro Eliminado");
            } else {
                JOptionPane.showMessageDialog(null, "Debe seleccionar un registro a eliminar");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Hay un problema al eliminar");
        }
    }

