    private void guardar() {
        if (cbLugar.getSelectedIndex() > 0) {
            if (!txtNombre.getText().equals("")) {
                try {
                    int idLugar = nLugar.getKey((String) cbLugar.getSelectedItem());
                    negocio.guardar(txtNombre.getText(), idLugar);
                    JOptionPane.showMessageDialog(null, "Registro Guardado");
                    actualizar();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Hay un problema al registrar", null, 2);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Debe rellenar el campo Nombre", "Atención", 1);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un lugar", "Atención", 1);
        }

    }

    private void modificar() {
        if (lbIdRecinto.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro de la tabla", "Atención", 1);
        } else {
            if (cbLugar.getSelectedIndex() > 0) {
                if (!txtNombre.getText().equals("")) {
                    try {
                        int idLugar = nLugar.getKey((String) cbLugar.getSelectedItem());
                        negocio.modificar(Integer.valueOf(lbIdRecinto.getText()), txtNombre.getText(), idLugar);
                        JOptionPane.showMessageDialog(null, "Registro Actualizado");
                        actualizar();
                    } catch (SQLException ex) {
                        JOptionPane.showMessageDialog(null, "Hay un problema al actualizar");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Debe rellenar el campo Nombre", "Atención", 1);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Debe seleccionar un lugar", "Atención", 1);
            }
        }
    }

    private void eliminar() {
        if (lbIdRecinto.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro de la tabla", "Atención", 1);
        } else {
            try {
                negocio.eliminar(Integer.valueOf(lbIdRecinto.getText()));
                JOptionPane.showMessageDialog(null, "Registro Eliminado");
                actualizar();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Hay un problema al eliminar, puede ser debido a que ya tiene usuarios y mesa asignados");
            }

        }
    }

