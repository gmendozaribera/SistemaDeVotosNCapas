    private void tbRecintoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbRecintoMouseClicked
        // TODO add your handling code here:
        int row = tbRecinto.rowAtPoint(evt.getPoint());
        lbIdRecinto.setText(tbRecinto.getValueAt(row, 0).toString());
        txtNombre.setText(tbRecinto.getValueAt(row, 1).toString());

        String lugar = tbRecinto.getValueAt(row, 2).toString();
        for (int i = 0; i < cbLugar.getItemCount(); i++) {
            if (lugar.equals(cbLugar.getItemAt(i))) {
                cbLugar.setSelectedIndex(i);
            }
        }
    }//GEN-LAST:event_tbRecintoMouseClicked

