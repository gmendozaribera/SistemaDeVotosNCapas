    private void guardar() {
        if (txtPaterno.equals("") || !negocio.isNumeric(txtCi.getText()) || txtNombre.equals("")
                || cbCandidatura.getSelectedIndex() == 0 || cbPartido.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Revise que esten correctamente llenados los campos", "Falta de datos", 0, null);
        } else {
            int idCandidatura = nCandidatura.getKey((String) cbCandidatura.getSelectedItem());
            int idPartido = nPartido.getKey((String) cbPartido.getSelectedItem());
            negocio.guardar(txtNombre.getText(), txtPaterno.getText(), txtMaterno.getText(), Integer.valueOf(txtCi.getText()), idCandidatura, idPartido);
            actualizar();
            JOptionPane.showMessageDialog(null, "Registro guardado exitosamente");
        }
    }

    private void modificar() {
        if (!negocio.isNumeric(lbIdCandidato.getText()) || txtPaterno.equals("") || !negocio.isNumeric(txtCi.getText()) || txtNombre.equals("")
                || cbCandidatura.getSelectedIndex() == 0 || cbPartido.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Revise que esten correctamente llenados los campos", "Falta de datos", 0, null);
        } else {
            int idCandidatura = nCandidatura.getKey((String) cbCandidatura.getSelectedItem());
            int idPartido = nPartido.getKey((String) cbPartido.getSelectedItem());
            negocio.modificar(Integer.valueOf(lbIdCandidato.getText()), txtNombre.getText(), txtPaterno.getText(), txtMaterno.getText(), Integer.valueOf(txtCi.getText()), idCandidatura, idPartido);
            actualizar();
            JOptionPane.showMessageDialog(null, "Registro actualizado exitosamente");
        }
    }

    private void eliminar() {
        if (!lbIdCandidato.getText().equals("")) {
            negocio.eliminar(Integer.valueOf(lbIdCandidato.getText()));
            JOptionPane.showMessageDialog(null, "Registro Eliminado");
            actualizar();
        }
    }

