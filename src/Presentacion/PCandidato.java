    private void tbCandidatoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCandidatoMouseClicked
        // TODO add your handling code here:
        int row = tbCandidato.rowAtPoint(evt.getPoint());
        lbIdCandidato.setText(tbCandidato.getValueAt(row, 0).toString());
        txtNombre.setText(tbCandidato.getValueAt(row, 1).toString());
        txtPaterno.setText(tbCandidato.getValueAt(row, 2).toString());
        txtMaterno.setText(tbCandidato.getValueAt(row, 3).toString());
        txtCi.setText(tbCandidato.getValueAt(row, 4).toString());
        String candidatura = tbCandidato.getValueAt(row, 5).toString();
        for (int i = 0; i < cbCandidatura.getItemCount(); i++) {
            if (candidatura.equals(cbCandidatura.getItemAt(i))) {
                cbCandidatura.setSelectedIndex(i);
            }
        }
        String partido = tbCandidato.getValueAt(row, 6).toString();
        for (int i = 0; i < cbPartido.getItemCount(); i++) {
            if (partido.equals(cbPartido.getItemAt(i))) {
                cbPartido.setSelectedIndex(i);
            }
        }
    }//GEN-LAST:event_tbCandidatoMouseClicked

