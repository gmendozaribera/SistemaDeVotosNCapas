    private void asignarCantidad() {
        if (negocio.isNumeric(txtCantidad.getText())) {
            int idRecinto = nRecinto.getKey((String) cbRecinto.getSelectedItem());
            if (idRecinto == -1) {
                JOptionPane.showMessageDialog(null, "Debes selecionar un recinto valido");
            } else {
                negocio.asignarCantidad(Integer.valueOf(txtCantidad.getText()), idRecinto);
                actualizar();
                JOptionPane.showMessageDialog(null, "Registro Guardado");
            }
        } else {
            JOptionPane.showMessageDialog(null, "La cantidad debe ser en valores enteros y que sea mayor a uno");
        }
    }

