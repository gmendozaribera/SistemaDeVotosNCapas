    private void tbMesaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMesaMouseClicked
        // TODO add your handling code here:
        int row = tbMesa.rowAtPoint(evt.getPoint());
        String recinto = tbMesa.getValueAt(row, 0).toString();
        for (int i = 0; i < cbRecinto.getItemCount(); i++) {
            if (recinto.equals(cbRecinto.getItemAt(i))) {
                cbRecinto.setSelectedIndex(i);
            }
        }
        txtCantidad.setText(tbMesa.getValueAt(row, 1).toString());


    }//GEN-LAST:event_tbMesaMouseClicked

